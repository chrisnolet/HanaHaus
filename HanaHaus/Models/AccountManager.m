#import "AccountManager.h"

@implementation AccountManager

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Class methods

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
+ (instancetype)sharedInstance
{
    static id sharedInstance;
    static dispatch_once_t onceToken;

    dispatch_once(&onceToken, ^{
        sharedInstance = [[self alloc] init];
    });

    return sharedInstance;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Public methods

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (NSString *)name
{
    return [[NSUserDefaults standardUserDefaults] objectForKey:kUserDefaultsAccountName];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setName:(NSString *)name
{
    [[NSUserDefaults standardUserDefaults] setObject:name forKey:kUserDefaultsAccountName];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (NSString *)email
{
    return [[NSUserDefaults standardUserDefaults] objectForKey:kUserDefaultsAccountEmail];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setEmail:(NSString *)email
{
    [[NSUserDefaults standardUserDefaults] setObject:email forKey:kUserDefaultsAccountEmail];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (NSString *)phone
{
    return [[NSUserDefaults standardUserDefaults] objectForKey:kUserDefaultsAccountPhone];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setPhone:(NSString *)phone
{
    [[NSUserDefaults standardUserDefaults] setObject:phone forKey:kUserDefaultsAccountPhone];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (NSString *)zip
{
    return [[NSUserDefaults standardUserDefaults] objectForKey:kUserDefaultsAccountZip];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setZip:(NSString *)zip
{
    [[NSUserDefaults standardUserDefaults] setObject:zip forKey:kUserDefaultsAccountZip];
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (NSString *)validate
{
    if ([self.name length] == 0) {
        return @"Name required";
    }

    if ([self.email length] == 0) {
        return @"Email required";
    }

    if ([self.phone length] == 0) {
        return @"Phone required";
    }

    if ([self.zip length] == 0) {
        return @"Zip required";
    }

    return nil;
}

@end
